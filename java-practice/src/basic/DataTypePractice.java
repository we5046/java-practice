package basic;

public class DataTypePractice {
    static void main() {

        int intNum;
        float floatNum;
        double doubleNum;
        char characterval;
        boolean bool;

        intNum = 120;
        floatNum = 12.23f;
        doubleNum = 12.23;
        characterval = 'a';
        bool = true;

        System.out.println("Data Practice Program");
        System.out.println("Integer Data Type");
        System.out.println(intNum);
        System.out.println("Float Data Type");
        System.out.println(floatNum);
        System.out.println("Double Data Type");
        System.out.println(doubleNum);
        System.out.println("Character Data Type");
        System.out.println(characterval);
        System.out.println("Boolean Data Type");
        System.out.println(bool);
    }
}
